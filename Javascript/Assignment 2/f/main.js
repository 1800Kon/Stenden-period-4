function doStuff() {
    var text = document.getElementById("text").value;
    var textLetters = text.toLowerCase().split("");
    var final = textLetters.map(function (instance) {
        return letters[instance];
    }).join(" ");
    document.getElementById("result").innerHTML = final;
}

